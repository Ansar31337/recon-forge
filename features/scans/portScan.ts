function probePort(
  host: string,
  port: number,
  timeoutMs: number,
): Promise<OpenPort | null> {
  return new Promise((resolve) => {
    const s = new net.Socket();
    let banner = "";
    s.setTimeout(timeoutMs);

    s.once("connect", () => {
      const t = setTimeout(() => {
        s.destroy();
        resolve({
          port,
          service: SERVICE_MAP[port] || "unknown",
          banner: banner.trim() || null,
        });
      }, 400);

      s.on("data", (d) => {
        banner += d.toString("utf8").slice(0, 120);
      });
      s.once("end", () => {
        clearTimeout(t);
        s.destroy();
        resolve({
          port,
          service: SERVICE_MAP[port] || "unknown",
          banner: banner.trim() || null,
        });
      });
    });

    s.once("timeout", () => {
      s.destroy();
      resolve(null);
    });
    s.once("error", () => {
      s.destroy();
      resolve(null);
    });
    s.connect(port, host);
  });
}

async function runConcurrent<T>(
  items: T[],
  worker: (t: T) => Promise<OpenPort | null>,
  concurrency: number,
): Promise<OpenPort[]> {
  const out: OpenPort[] = [];
  let i = 0;
  await Promise.all(
    Array.from({ length: concurrency }, async () => {
      while (i < items.length) {
        const idx = i++;
        const res = await worker(items[idx]);
        if (res) out.push(res);
      }
    }),
  );
  return out.sort((a, b) => a.port - b.port);
}

export async function portScan(
  host: string,
  profile: PortProfile,
): Promise<OpenPort[]> {
  if (profile === "top1000" && process.env.PORTSCAN_ALLOW_TOP1000 !== "true") {
    profile = "top100";
  }
  if (
    profile === "full65535" &&
    process.env.PORTSCAN_ALLOW_FULL65535 !== "true"
  ) {
    profile = "top100";
  }

  const ports =
    profile === "top100"
      ? TOP_100
      : profile === "top1000"
        ? TOP_1000
        : Array.from({ length: 65535 }, (_, i) => i + 1);
  const timeoutMs = Number(process.env.PORTSCAN_TIMEOUT_MS || 1500);
  const concurrency =
    profile === "full65535" ? 150 : profile === "top1000" ? 75 : 30;

  return runConcurrent(
    ports,
    (p) => probePort(host, p, timeoutMs),
    concurrency,
  );
}
